class Solution {
  public:
    int stockBuyAndSell(int n, vector<int> &prices) {
        // code here
        // ode here
        int ans=0;
        int i=1;
        int buy=prices[0];
        while(i<n){
            if(prices[i]>buy && prices[i]>=prices[i-1]){
                i++;
            }
            else{
                ans+=(prices[i-1]-buy);
                buy=prices[i];
                i++;
            }
        }
        ans+=(prices[i-1]-buy);
        return ans;
    }
};


